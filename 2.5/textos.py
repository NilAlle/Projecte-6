# Descripció: Crea un mòdul textos.py amb una funció que posi en majúscules, minúscules i capitalitzi textos.
# Especificacions de entrada: 

def majuscules(text):
    return text.upper()


def minuscules(text):
    return text.lower()


def capitalitzat(text):
    return text.capitalize()
